class Furniture{

 static int furnitureId;
 static String brand;
 static String color;
 static double price;
 
 //Setter and getter
  public static void setFurnitureId(int id){
 
 furnitureId = id;
 
 }
  public static int getFurnitureId(){
 
  return furnitureId;

}
 public static void setBrand(String bd){
 
 brand = bd;
 }
 public static String getBrand(){
 
  return brand;
}

public static void setColor(String clr){
 
 color = clr;
}
public static String getColor(){
	
   return color;   
  
}
public static void setPrice(double prs){
 
 price = prs;
}
public static double getPrice(){
	
   return price;   
  
}







}