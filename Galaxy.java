class Galaxy{

 static int galaxyId;
 static String name;
 static String color;
 static int magnitude;
 
 //Setter and getter
  public static void setGalaxyId(int id){
 
 galaxyId = id;
 
 }
  public static int getGalaxyId(){
 
  return galaxyId;

}
 public static void setName(String nm){
 
 name = nm;
 }
 public static String getName(){
 
  return name;
}

public static void setColor(String clr){
 
 color = clr;
}
public static String getColor(){
	
   return color;   
  
}
public static void setMagnitude(int mg){
 
  magnitude= mg;
}
public static int getMagnitude(){
	
   return magnitude;   
  
}







}